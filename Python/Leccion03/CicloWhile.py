# Imprimir numeros del 0 al 5 con cicli while
print("Aumentando los numeros hasta 5")
maximo = 5
contador = 0

while contador <= maximo:
    print(contador)
    contador += 1

print("--------------------------")

# Imprimir numeros desde 5 al 1
print(("Imprimimos los numeros del 5 al uno"))
minimo = 1
contador = 5

while contador >= minimo:
    print(contador)
    contador -= 1
print("**** Fin ****")