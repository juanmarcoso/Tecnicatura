# Operadores de comparacion
d = 4
b = 2

resultado = (d == b)  # Comprobamos si son iguales
print(resultado)

# Operador diferente
resultado = d != b
print(resultado)

# Operador Mayor que
resultado = d > b
print(resultado)

# Operador Menor que
resultado = d < b
print(resultado)

# Operador menor o igual que
resultado = d <= d
print(resultado)

# Operador mayor o igual que
resultado = d >= d
print(resultado)